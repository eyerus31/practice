package interfac_examplw;

public abstract class Shape {
	private int vartices;
	private int sizes;
	String name;
	public int getVartices() {
		return vartices;
	}
	public void setVartices(int vartices) {
		this.vartices = vartices;
	}
	public int getSizes() {
		return sizes;
	}
	public void setSizes(int sizes) {
		this.sizes = sizes;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
